function regex(str) {
  if (/[^\\]\\Z/.test(str)) return false;

  try {
    new RegExp(str);

    return true;
  } catch (e) {
    return false;
  }
}

const keyword: IKeyword = {
  name: 'format',
  compile(compile, schema: any) {
    if (typeof schema !== 'string') {
      throw new Error('The schema of the "format" keyword should be a string.');
    }

    if (!formats[schema]) {
      throw new Error('Unknown string format supplied.');
    }

    const checkFormatFn = formats[schema] instanceof RegExp
      ? (value) => formats[schema].test(value)
      : formats[schema];

    return async (ref) => {
      const value = ref.value;

      if (utils.checkDataType('string', value)) {
        if (checkFormatFn(value)) {
          return utils.createSuccessResult();
        }

        return utils.createErrorResult(
          new ValidationMessage(
            false,
            keyword.name,
            'Should match format "{format}"',
            { format: schema },
          ),
        );
      }

      return undefined;
    };
  },
};

export default keyword;

